<?php
// Arithmetic Operator
$angka_1 = 10;
$angka_2 = 20;


// + Addition
echo 'Addition : ' . $angka_1 + $angka_2;
echo '<br>';
// - Subtraction
echo 'Subtraction : ' . $angka_1 - $angka_2;
echo '<br>';
// (/) Division
echo 'Division : ' . $angka_1 / $angka_2;
echo '<br>';
// * Multiply
echo 'Multiply : ' . $angka_1 * $angka_2;
echo '<br>';
// % Modulus
echo 'Modulus : ' . $angka_1 % $angka_2; 
echo '<br>';
// ** Exponentiation
echo 'Exponentiation : ' . $angka_1 ** $angka_2;
?>